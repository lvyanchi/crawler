package cn.yclv.model.jd;

public class PriceResult {
	private String id;
	private Double p;
	private Double m;
	private Double op;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Double getP() {
		return p;
	}

	public void setP(Double p) {
		this.p = p;
	}

	public Double getM() {
		return m;
	}

	public void setM(Double m) {
		this.m = m;
	}

	public Double getOp() {
		return op;
	}

	public void setOp(Double op) {
		this.op = op;
	}

}
